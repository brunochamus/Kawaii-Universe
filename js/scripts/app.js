//Funcion para agregar card de tops manga
function showHideManga() {
    const cardContainer = document.getElementById('card-content');

    if (cardContainer.children.length === 1) {
        manga.forEach(data => {
            const newDiv = document.createElement('div');
            newDiv.className = 'card';
            newDiv.innerHTML = `
                <div class="card-text">
                    <h3>${data.title}</h3>
                    <p>${data.description}</p>
                </div>
                <img src="${data.image}" alt="${data.title}">
            `;
            cardContainer.appendChild(newDiv);
        });
    }

    const cards = document.querySelectorAll('.card');
    cards.forEach(card => {
        card.style.display = (card.style.display === 'none' || card.style.display === '') ? 'flex' : 'none';
    });
}

//Funcion para agregar card de tops anime
function showHideAnime() {
    const cardContainerAnime = document.getElementById('cont-anime-takeoff');

    if (cardContainerAnime.children.length === 1) {
        anime.forEach(data => {
            const newDiv = document.createElement('div');
            newDiv.className = 'card-anime-takeoff';
            newDiv.innerHTML = `
                <div class="text-anime-takeoff">
                    <h3>${data.title}</h3>
                    <p>${data.description}</p>
                </div>
                <img src="${data.image}" alt="${data.title}">
            `;
            cardContainerAnime.appendChild(newDiv);
        });
    }

    const card = document.querySelectorAll('.card-anime-takeoff');
    card.forEach(cards => {
        cards.style.display = (cards.style.display === 'none' || cards.style.display === '') ? 'flex' : 'none';
    });
}

//Ver mas cards mobile
const cardContainers = document.querySelectorAll('.text-container');

cardContainers.forEach(container => {
    const hiddenText = container.querySelector('.hidden-text');
    const showMoreButton = container.querySelector('.show-more-button');
    const scrollXContainer = container.closest('.scroll-x'); // Encontrar el contenedor .scroll-x más cercano

    showMoreButton.addEventListener('click', function () {
        if (hiddenText.style.maxHeight) {
            hiddenText.style.maxHeight = null;
            showMoreButton.textContent = 'Ver más';
            // Restaurar la altura del contenedor .scroll-x
            scrollXContainer.style.height = '50rem';
        } else {
            hiddenText.style.maxHeight = hiddenText.scrollHeight + 'px';
            showMoreButton.textContent = 'Ver menos';
            // Ajustar la altura del contenedor .scroll-x para adaptarse al contenido expandido
            scrollXContainer.style.height = scrollXContainer.scrollHeight + hiddenText.scrollHeight + 'px';
        }
    });
});


